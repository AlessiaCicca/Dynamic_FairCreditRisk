# ── CLI ───────────────────────────────────────────────────────────────────────

def parse_args():
    p = argparse.ArgumentParser(
        description="Run simulation experiment (fair/unfair/direct/proxy)."
    )
    p.add_argument("--data_dir", required=True,
                   help="Directory containing data_{scenario}.csv and test files")
    p.add_argument("--scenario", default="fair",
                   choices=["fair", "unfair", "direct", "proxy", "temporal"])
    p.add_argument("--config", default=None,
                   help="Path to YAML config (overrides config.py defaults)")
    p.add_argument("--grid_search", action="store_true",
                   help="Run grid search after CV")
    p.add_argument("--out_dir", default=None,
                   help="Output directory (default: outputs/simulation/{scenario})")
    return p.parse_args()


def load_config(config_path: str, args) -> dict:
    """Load YAML config and merge with defaults from config.py."""
    cfg = dict(
        alpha=ALPHA, beta=BETA, gamma=GAMMA,
        eo_mode_d=EO_MODE_D, eo_mode_p=EO_MODE_P,
        schedule_mode_d=SCHEDULE_MODE_D, schedule_mode_p=SCHEDULE_MODE_P,
        horizon=HORIZON, landmarks=LANDMARKS,
        n_folds=N_FOLDS, use_wandb=USE_WANDB,
        grid_betas=GRID_BETAS, grid_alphas=GRID_ALPHAS, grid_gammas=GRID_GAMMAS,
    )
    if config_path and os.path.exists(config_path):
        with open(config_path) as f:
            overrides = yaml.safe_load(f)
        cfg.update(overrides or {})
    return cfg


# ── Data loading ──────────────────────────────────────────────────────────────

def load_raw(data_dir: str, scenario: str) -> pd.DataFrame:
    """Load and prepare the raw simulation panel."""
    path = os.path.join(data_dir, f"data_{scenario}.csv")
    df   = pd.read_csv(path)
    print(f"  Rows: {len(df):,}  |  Subjects: {df[ID_COL].nunique():,}")

    df = df.sort_values([ID_COL, TIME_COL])

    # Trend features
    trend_cols = []
    for col in ["X4", "X6"]:
        name = f"{col}_trend"
        df[name] = (
            df.groupby(ID_COL)[col]
            .transform(lambda x: x - x.shift(2))
        ).clip(-5, 5).fillna(0)
        trend_cols.append(name)

    for c in ALL_NUM_COLS + trend_cols + [TIME_COL]:
        df[c] = pd.to_numeric(df[c], errors="coerce").astype("float32")

    # Sensitive attribute per loan
    sens_per_id = df.groupby(ID_COL)[SENS_COL].first().rename("sens_loan")
    df = df.merge(sens_per_id, on=ID_COL, how="left")

    # FirstEventTime
    first_event = (
        df[df[EVENT_COL] == 1]
        .groupby(ID_COL)[TIME_COL].min()
        .rename("FirstEventTime")
    )
    df = df.merge(first_event, on=ID_COL, how="left")

    n_ids    = df[ID_COL].nunique()
    n_events = df.groupby(ID_COL)["FirstEventTime"].first().notna().sum()
    print(f"  Subjects: {n_ids:,}  |  Events: {n_events:,} ({n_events/n_ids:.1%})")

    return df, trend_cols


# ── Fairness analysis ─────────────────────────────────────────────────────────

def run_fairness_analysis(
    y_static, static_oof, sens_static,
    y_dynamic, dynamic_oof, sens_dynamic, lmk_vals,
    y_pp, pp_oof, sens_pp, pp_ages,
    out_dir: Path, cfg: dict,
) -> dict:
    """Compute and save all fairness metrics."""

    th_static  = compute_threshold(y_static,  static_oof)
    th_dynamic = compute_threshold(y_dynamic, dynamic_oof)
    th_pp      = compute_threshold(y_pp,      pp_oof)

    ybin_static  = (static_oof  >= th_static ).astype(int)
    ybin_dynamic = (dynamic_oof >= th_dynamic).astype(int)
    ybin_pp      = (pp_oof      >= th_pp     ).astype(int)

    # ── Aggregate ─────────────────────────────────────────────────────────────
    agg_rows = []
    for mname, y_t, y_p, y_b, sens, th in [
        ("M_STATIC",  y_static,  static_oof,  ybin_static,  sens_static,  th_static),
        ("M_DYNAMIC", y_dynamic, dynamic_oof, ybin_dynamic, sens_dynamic, th_dynamic),
        ("M_PP",      y_pp,      pp_oof,      ybin_pp,      sens_pp,      th_pp),
    ]:
        yt_f, yp_f, sn_f = filter_sensitive(y_t, y_p, sens)
        yb_f = (yp_f >= th).astype(int)
        res  = fairness_metrics(yt_f, yp_f, yb_f, sn_f, GROUP_NAMES, threshold=th)
        print_fairness_report(mname, res, GROUP_NAMES, label="AGGREGATE")
        agg_rows.append(res_to_row(res, GROUP_NAMES, {"model": mname}))

    df_agg = pd.DataFrame(agg_rows)
    df_agg.to_csv(out_dir / "fairness_aggregate.csv", index=False)

    # ── Dynamic per landmark ──────────────────────────────────────────────────
    dyn_rows = []
    for L in cfg["landmarks"]:
        mask = lmk_vals == L
        if mask.sum() < 20: continue
        yt_f, yp_f, sn_f = filter_sensitive(
            y_dynamic[mask], dynamic_oof[mask], sens_dynamic[mask]
        )
        if len(np.unique(yt_f)) < 2 or len(np.unique(sn_f)) < 2: continue
        yb_f = (yp_f >= th_dynamic).astype(int)
        res  = fairness_metrics(yt_f, yp_f, yb_f, sn_f, GROUP_NAMES, threshold=th_dynamic)
        print_fairness_report("M_DYNAMIC", res, GROUP_NAMES, label=f"landmark={L}")
        dyn_rows.append(res_to_row(res, GROUP_NAMES,
                                   {"model": "M_DYNAMIC", "landmark": L}))

    df_dyn_lmk = pd.DataFrame(dyn_rows)
    df_dyn_lmk.to_csv(out_dir / "fairness_dynamic_by_landmark.csv", index=False)

    # ── PP per time ───────────────────────────────────────────────────────────
    pp_rows = []
    for T in sorted(np.unique(pp_ages)):
        mask = pp_ages == T
        if mask.sum() < 20: continue
        yt_f, yp_f, sn_f = filter_sensitive(
            y_pp[mask], pp_oof[mask], sens_pp[mask]
        )
        if len(np.unique(yt_f)) < 2 or len(np.unique(sn_f)) < 2: continue
        yb_f = (yp_f >= th_pp).astype(int)
        res  = fairness_metrics(yt_f, yp_f, yb_f, sn_f, GROUP_NAMES, threshold=th_pp)
        pp_rows.append(res_to_row(res, GROUP_NAMES,
                                  {"model": "M_PP", "time": int(T)}))

    df_pp_time = pd.DataFrame(pp_rows)
    df_pp_time.to_csv(out_dir / "fairness_pp_by_time.csv", index=False)

    # ── adTPR / adFPR ──────────────────────────────────────────────────────────
    print("\n--- adTPR / adFPR ---")
    for mname, y_t, y_b, sens, tpts in [
        ("M_STATIC",  y_static,  ybin_static,  sens_static,  None),
        ("M_DYNAMIC", y_dynamic, ybin_dynamic, sens_dynamic, lmk_vals),
        ("M_PP",      y_pp,      ybin_pp,      sens_pp,      pp_ages),
    ]:
        res = compute_adTPR_adFPR(y_t, y_b, sens, tpts)
        print(f"  {mname:<12} — adTPR={res['adTPR']:.4f}  adFPR={res['adFPR']:.4f}")

    # ── AUC fairness ──────────────────────────────────────────────────────────
    df_auc = auc_fairness_single_attr(
        df_dynamic=df_dyn_lmk, df_pp=df_pp_time, df_static_agg=df_agg,
        time_col_dyn="landmark", time_col_pp="time",
    )
    df_auc.to_csv(out_dir / "fairness_auc_comparison.csv", index=False)
    print("\n=== AUC FAIRNESS ===")
    print(df_auc.to_string(index=False))

    # ── Plots ─────────────────────────────────────────────────────────────────
    static_vals = {
        k: df_agg[df_agg["model"] == "M_STATIC"][k].values[0]
        for k in ["independence", "separation", "sufficiency"]
        if k in df_agg.columns
    }

    plot_fairness_over_time_single(
        df_time=df_dyn_lmk, time_col="landmark",
        title=f"Fairness — M_DYNAMIC  [{ATTR_NAME}]",
        filename="fairness_dynamic_over_landmark.png",
        out_dir=out_dir, static_val_dict=static_vals, min_samples_per_group=20,
    )
    plot_fairness_over_time_single(
        df_time=df_pp_time, time_col="time",
        title=f"Fairness — M_PP  [{ATTR_NAME}]",
        filename="fairness_pp_over_time.png",
        out_dir=out_dir, static_val_dict=static_vals, min_samples_per_group=5,
    )
    plot_auc_fairness_bar(df_auc=df_auc, out_dir=out_dir,
                          filename="fairness_auc_comparison.png")

    print(f"\n✓ Fairness outputs saved in: {out_dir}")
    return dict(df_agg=df_agg, df_dyn_lmk=df_dyn_lmk,
                df_pp_time=df_pp_time, df_auc=df_auc)


# ── Main ──────────────────────────────────────────────────────────────────────

def main():
    args = parse_args()
    cfg  = load_config(args.config, args)

    # Output directory
    out_dir = Path(args.out_dir) if args.out_dir else \
              Path("outputs") / "simulation" / args.scenario
    out_dir.mkdir(parents=True, exist_ok=True)

    run_tag = (
        f"simulation_{args.scenario}"
        f"_S:{cfg['beta']}"
        f"_D:{cfg['alpha']}_{cfg['eo_mode_d']}"
        f"_P:{cfg['gamma']}_{cfg['eo_mode_p']}"
    )

    print(f"\n{'='*60}")
    print(f"  Scenario : {args.scenario}")
    print(f"  Data dir : {args.data_dir}")
    print(f"  Out dir  : {out_dir}")
    print(f"  Device   : {DEVICE}")
    print(f"{'='*60}\n")

    # ── Load raw data ─────────────────────────────────────────────────────────
    print("Loading raw data...")
    df, trend_cols = load_raw(args.data_dir, args.scenario)

    enc_cat = OneHotEncoder(handle_unknown="ignore",
                             sparse_output=False, dtype=np.float32)
    enc_cat.fit(df[CAT_COLS])

    # ── Build datasets ────────────────────────────────────────────────────────
    print("\nBuilding PERSON-PERIOD dataset...")
    pp_data = build_person_period(
        df=df,
        static_cols=STATIC_COLS, tvc_cols=TVC_COLS,
        trend_cols=trend_cols, cat_cols=CAT_COLS,
        id_col=ID_COL, time_col=TIME_COL,
        event_col=EVENT_COL, first_event_col="FirstEventTime",
        sens_col="sens_loan", enc_cat=enc_cat,
    )

    print("\nBuilding STATIC dataset...")
    static_data = build_static(
        df=df,
        static_cols=STATIC_COLS, cat_cols=CAT_COLS,
        horizon=cfg["horizon"],
        id_col=ID_COL, time_col=TIME_COL,
        first_event_col="FirstEventTime",
        sens_col="sens_loan", enc_cat=enc_cat,
    )

    print("\nBuilding DYNAMIC dataset...")
    dynamic_data = build_dynamic(
        df=df,
        static_cols=STATIC_COLS, tvc_cols=TVC_COLS,
        cat_cols=CAT_COLS, landmarks=cfg["landmarks"],
        horizon=cfg["horizon"],
        id_col=ID_COL, time_col=TIME_COL,
        first_event_col="FirstEventTime",
        sens_col="sens_loan", enc_cat=enc_cat,
    )

    del df; gc.collect()

    # ── Cross-validation ──────────────────────────────────────────────────────
    train_kwargs = dict(
        beta=cfg["beta"], alpha=cfg["alpha"], gamma=cfg["gamma"],
        eo_mode_d=cfg["eo_mode_d"], eo_mode_p=cfg["eo_mode_p"],
        schedule_mode_d=cfg["schedule_mode_d"],
        schedule_mode_p=cfg["schedule_mode_p"],
    )

    print("\nTraining M_STATIC...")
    res_static = run_cv(
        X=static_data["X"], y=static_data["y"],
        groups=static_data["groups"], sensitive=static_data["sensitive"],
        model_name="static", n_splits=cfg["n_folds"], **train_kwargs,
    )

    print("\nTraining M_DYNAMIC...")
    res_dynamic = run_cv(
        X=dynamic_data["X"], y=dynamic_data["y"],
        groups=dynamic_data["groups"], sensitive=dynamic_data["sensitive"],
        time_arr=dynamic_data["lmk_vals"], subj_ids=dynamic_data["groups"],
        model_name="dynamic", n_splits=cfg["n_folds"],
        landmarks=cfg["landmarks"], **train_kwargs,
    )

    print("\nTraining M_PP...")
    res_pp = run_cv(
        X=pp_data["X"], y=pp_data["y"],
        groups=pp_data["groups"], sensitive=pp_data["sensitive"],
        time_arr=pp_data["ages"], subj_ids=pp_data["groups"],
        model_name="person_period", n_splits=cfg["n_folds"], **train_kwargs,
    )

    # ── Summary ───────────────────────────────────────────────────────────────
    summary = build_summary_table({
        "M_STATIC":  res_static,
        "M_DYNAMIC": res_dynamic,
        "M_PP":      res_pp,
    })
    print("\n=== CV RESULTS ===")
    print(summary.to_string(index=False))
    summary.to_csv(out_dir / "cv_results.csv", index=False)

    lmk_summary = build_landmark_summary(res_dynamic, cfg["landmarks"])
    if not lmk_summary.empty:
        print("\n=== DYNAMIC — AUC PER LANDMARK ===")
        print(lmk_summary.to_string(index=False))
        lmk_summary.to_csv(out_dir / "dynamic_by_landmark_cv.csv", index=False)

    # ── Fairness analysis ─────────────────────────────────────────────────────
    print("\n" + "="*60)
    print("FAIRNESS ANALYSIS")
    print("="*60)

    run_fairness_analysis(
        y_static=static_data["y"],
        static_oof=res_static["oof_preds"],
        sens_static=static_data["sensitive"],
        y_dynamic=dynamic_data["y"],
        dynamic_oof=res_dynamic["oof_preds"],
        sens_dynamic=dynamic_data["sensitive"],
        lmk_vals=dynamic_data["lmk_vals"],
        y_pp=pp_data["y"],
        pp_oof=res_pp["oof_preds"],
        sens_pp=pp_data["sensitive"],
        pp_ages=pp_data["ages"],
        out_dir=out_dir, cfg=cfg,
    )

    # ── Grid search ───────────────────────────────────────────────────────────
    if args.grid_search:
        print("\n" + "="*60)
        print("GRID SEARCH")
        print("="*60)

        df_grid = run_grid_search(
            X_static=static_data["X"], y_static=static_data["y"],
            grp_static=static_data["groups"], sens_static=static_data["sensitive"],
            X_dynamic=dynamic_data["X"], y_dynamic=dynamic_data["y"],
            grp_dynamic=dynamic_data["groups"], sens_dynamic=dynamic_data["sensitive"],
            lmk_vals=dynamic_data["lmk_vals"],
            X_pp=pp_data["X"], y_pp=pp_data["y"],
            grp_pp=pp_data["groups"], sens_pp=pp_data["sensitive"],
            pp_ages=pp_data["ages"],
            group_names=GROUP_NAMES,
            betas=cfg["grid_betas"], alphas=cfg["grid_alphas"],
            gammas=cfg["grid_gammas"],
            n_folds=cfg["n_folds"],
            eo_mode_d=cfg["eo_mode_d"], eo_mode_p=cfg["eo_mode_p"],
            out_dir=out_dir, run_tag=run_tag,
        )
        plot_tradeoff(df_grid, out_dir=out_dir, run_tag=run_tag)

    print(f"\n✓ All outputs saved in: {out_dir}")


if __name__ == "__main__":
    main()
